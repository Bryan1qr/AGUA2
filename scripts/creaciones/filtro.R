    filter(!UNIDADES %in% c("", "-", "UNIDADES", NA)) %>% arrange(PARAMETROS) %>% 
    mutate(PARAMETROS = case_when(
      PARAMETROS %in% c("Aceites y grasas") ~ "Aceites y Grasas",

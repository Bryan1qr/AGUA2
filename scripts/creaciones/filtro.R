      PARAMETROS %in% c("Calcio (Ca tot)", "Calcio total (Ca tot)") ~ "Calcio",
      PARAMETROS %in% c("Carbonatos") ~ "Carbonatos",
      PARAMETROS %in% c("Cianuro libre") ~ "Cianuro Libre",
      PARAMETROS %in% c("Cianuro WAD ") ~ "Cianuro WAD",
      PARAMETROS %in% c("Cloruros") ~ "Cloruros",
      PARAMETROS %in% c("Cobalto (Co tot)", "Cobalto total (Co tot)") ~ "Cobalto",
      PARAMETROS %in% c("Cobre (Cu tot)", "Cobre total (Cu tot)") ~ "Cobre",
      PARAMETROS %in% c("Coliformes Termotolerantes", "Coliformes termotolerantes ") ~ "Coliformes Termotolerantes",

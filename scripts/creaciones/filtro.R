      PARAMETROS %in% c("Demanda Química de Oxígeno") ~ "Demanda Química de Oxígeno (DQO)",
      PARAMETROS %in% c("Escherichia coli") ~ "Escherichia coli",
      PARAMETROS %in% c("Estaño") ~ "Estaño",
      PARAMETROS %in% c("Estroncio") ~ "Estroncio",
      PARAMETROS %in% c("Fenoles") ~ "Fenoles",
      PARAMETROS %in% c("Fluoruros, F-") ~ "Fluoruros",
      PARAMETROS %in% c("Fosfatos (PO4)") ~ "Fosfatos",
      PARAMETROS %in% c("Fósforo total (P tot)", "Fósforo total)") ~ "Fósforo Total",
      PARAMETROS %in% c("Fósforo (P)") ~ "Fósforo (P)",

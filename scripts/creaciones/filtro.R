      PARAMETROS %in% c("Conductividad", "Conductividad ", "Conductividad (Cond,)") ~ "Conductividad",
      PARAMETROS %in% c("Cromo (Cr tot)", "Cromo total (Cr tot)") ~ "Cromo Total",
